    @Override
    public void run() {
        int i = 0;
        while (i < 10 && executando) {
            i++;
            try {
                Thread.sleep(1000);

            } catch (InterruptedException ex) {
                Logger.getLogger(TelaTotem.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (executando) {
            this.setVisible(false);
             this.dispose();
        }

    }

    private void voltar() {
        executando = true;
        threadVoltar = new Thread(this);
        threadVoltar.start();

    }

    public void cancelar() {
        executando = false;
    }
