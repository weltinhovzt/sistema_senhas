    public SenhaCaixa gerandoSenhaCaixaNormal() {
        SenhaCaixaDAO dao = new SenhaCaixaDAO();
        SenhaCaixa senhacaixa = new SenhaCaixa();
        if (dao.selectSenhaCaixaNormal().isEmpty()) {
            senhacaixa.setNumeroSenha(1);
            senhacaixa.setSetor(Setor.CAIXA);
            senhacaixa.setTipoAtendimento(TipoAtendimento.CONVENCIONAL);
            senhacaixa.setSigla(SiglaSetor.CC);
            senhacaixa.setDataSenha(data);
            senhacaixa.setHoraSenha(hora);
            senhacaixa.setStatusSenha(StatusSenha.AGUARDANDO);

            dao.inserirSenhaCaixa(senhacaixa);
     
        } else {
            SenhaCaixa ultimaSenha = dao.selectSenhaCaixaNormal().get(dao.selectSenhaCaixaNormal().size() - 1);
            int senha = ultimaSenha.getNumeroSenha();
            senhacaixa.setNumeroSenha(senha + 1);
            senhacaixa.setSetor(Setor.CAIXA);
            senhacaixa.setTipoAtendimento(TipoAtendimento.CONVENCIONAL);
            senhacaixa.setSigla(SiglaSetor.CC);
            senhacaixa.setDataSenha(data);
            senhacaixa.setHoraSenha(hora);
            senhacaixa.setStatusSenha(StatusSenha.AGUARDANDO);
           
            dao.inserirSenhaCaixa(senhacaixa);
            
            

        }
        return senhacaixa;
    }

