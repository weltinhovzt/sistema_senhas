    public void validarLogin() {
        UsuarioDAO dao = new UsuarioDAO();
        // Validar Campos em branco
        if (jTUsuario.getText().equals("") || jPSenha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Favor Preencher os campos Obrigatorios");
        } else {
            //Validar o Usuario no banco de dados caso estiver certo abre tela inicial
            if (dao.validarUsuarios(jTUsuario.getText(), jPSenha.getText()) == null) {
                JOptionPane.showMessageDialog(null, "Usuario ou Senha incorretos");
            } else {
                TelaChamarSenha tela = new TelaChamarSenha(dao.validarUsuarios(jTUsuario.getText(), jPSenha.getText()));
                tela.setVisible(true);
                this.dispose();

            }

        }
    }

